console.log("Operators in Js")
let a = 45;
let b = 4;
console.log("Various Types of operator")
console.log("a + b = ", a + b)
console.log("a - b = ", a - b)
console.log("a / b = ", a / b)
console.log("a * b = ", a * b)
console.log("a ** b = ", a ** b) //  a to power b
console.log("a % b = ", a % b) // gives remainder

console.log("Increment Operator")
console.log("a++ = ", a++)
console.log("value of a", a)
console.log("++a = ", ++a)
console.log("value of a", a)

console.log("Decrement Operator")
console.log("a-- =", a--)
console.log("value of a", a)
console.log("--a = ", --a)
console.log("value of a", a)

// Assignment Operator

let c = 50;
c += 5
console.log("c+=5", c) // c = c+5
c -= 5
console.log("c-=5", c) // c = c-5
c *= 5
console.log("c*=5", c) // c = c*5
c /= 5
console.log("c/=5", c) // c = c/5

// Comparsion Operator
let comp1 = 6
let comp2 = "6"
console.log("comp1 == comp2", comp1 == comp2)
console.log("comp1 != comp2", comp1 != comp2)
console.log("comp1 === comp2", comp1 === comp2)
console.log("comp1 !== comp2", comp1 !== comp2)

